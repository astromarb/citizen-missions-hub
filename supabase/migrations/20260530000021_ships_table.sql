-- ============================================================
-- Ships reference table (net-worth fleet values)
-- Migrated from src/data/ships.js so admins can edit values live.
-- ============================================================

CREATE TABLE IF NOT EXISTS public.ships (
  id            BIGSERIAL PRIMARY KEY,
  name          TEXT      NOT NULL UNIQUE,
  manufacturer  TEXT      NOT NULL DEFAULT '',
  value         BIGINT    NOT NULL DEFAULT 0,
  sort_order    INT       NOT NULL DEFAULT 0
);

ALTER TABLE public.ships ENABLE ROW LEVEL SECURITY;

CREATE POLICY "ships_select" ON public.ships FOR SELECT TO authenticated USING (true);
CREATE POLICY "ships_admin_write" ON public.ships FOR ALL TO authenticated
  USING (EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true))
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = true));

